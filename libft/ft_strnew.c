#include <stdlib.h>
#include "libft.h"

char	*ft_strnew(size_t size)
{
	char *str;

	str = ft_memalloc(size + 1);
	if (str == 0)
		return (NULL);
	return (ft_memset(str, 0, size + 1));
}
