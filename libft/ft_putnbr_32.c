#include "libft.h"

void	ft_putnbr_32(uint32_t n)
{
	if (n < 10)
	{
		ft_putchar(n + 48);
		return ;
	}
	ft_putnbr_32(n / 10);
	ft_putchar((n % 10) + 48);
	return ;
}
