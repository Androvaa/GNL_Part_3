#include "libft.h"

int		is_flag(char c)
{
	if (c == '0')
		return (1);
	if (c == ' ')
		return (2);
	if (c == '#')
		return (3);
	if (c == '-')
		return (4);
	if (c == '+')
		return (5);
	return (0);
}
