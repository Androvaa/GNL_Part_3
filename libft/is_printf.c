#include "libft.h"

int		is_printf(char c)
{
	c = ft_tolower(c);
	if (c == 's' || c == 'p' || c == 'd' || c == 'i'
			|| c == 'o' || c == 'u' || c == 'x' || c == 'c')
		return (1);
	return (0);
}
